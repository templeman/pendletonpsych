<h3>Residency in Psychology</h3>
<p>Psychological Services of Pendleton, LLC offers a psychologist residency to meet requirements for licensure in Oregon. Supervised experience in assessment and psychotherapy is provided by licensed psychologists. Applicants must have doctorates in psychology from psychology programs approved by the Oregon Board of Psychologist Examiners.</p>
