<h2>Education Opportunities</h2>
<h3>Practicum in Psychology</h3>
<p>Psychological Services of Pendleton, LLC provides educational
opportunities for graduate students in clinical psychology. Supervised
experience in assessment and psychotherapy is available to students
interested in practicing in a rural setting. Applicants should be
currently enrolled in a graduate program in clinical psychology.</p><p>Email
Dr. Templeman at <a
  href="mailto:opportunities@pendletonpsych.com?subject=Education%20Opportunities%20at%20PSP">opportunities@pendletonpsych.com</a>
for more information.</p>

<h3>Residency in Psychology</h3>
<p>Psychological Services of Pendleton, LLC offers a psychologist
residency to meet requirements for licensure in Oregon. Supervised
experience in assessment and psychotherapy is provided by licensed
psychologists. Applicants must have doctorates in psychology from
psychology programs approved by the Oregon Board of Psychologist
Examiners.</p><p>Email Dr. Templeman at <a
  href="mailto:opportunities@pendletonpsych.com?subject=Education%20Opportunities%20at%20PSP">opportunities@pendletonpsych.com</a>
for information about terms and earnings.</p>
